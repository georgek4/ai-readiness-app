// Export: Individual Assessment Results
// ============================================================
export function exportResultsPDF(assessment, department, tier, gapAnalysis) {
  const doc = createPDF('Assessment Results');
  let y = addHeader(doc, 'AI Readiness Assessment Results', `${department?.name || ''} — ${assessment.role}`);

  // Score overview
  y = addSectionTitle(doc, y, 'Overall Score');
  doc.setFontSize(36);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(...COLORS.primary);
  doc.text(`${assessment.overallScore}`, 50, y + 14, { align: 'center' });
  doc.setFontSize(12);
  doc.text('/ 100', 66, y + 14);

  doc.setFontSize(16);
  const tierColor = tier?.color ? hexToRgb(tier.color) : COLORS.primary;
  doc.setTextColor(...tierColor);
  doc.text(tier?.name || '', 100, y + 6);
  doc.setFontSize(10);
  doc.setTextColor(100, 100, 100);
  doc.text(tier?.description || '', 100, y + 14);

  y += 22;

  // XP and Badges
  doc.setFontSize(10);
  doc.setTextColor(...COLORS.amber);
  doc.text(`XP: ${assessment.xpTotal || 0}`, 14, y);
  if (assessment.badges?.length) {
    doc.setTextColor(...COLORS.purple);
    doc.text(`Badges: ${assessment.badges.join(', ')}`, 60, y);
  }
  y += 10;

  // Level Scores
  y = addSectionTitle(doc, y, 'Dimension Scores');
  const dimensions = [
    { key: 'awareness', label: 'AI Awareness' },
    { key: 'currentUsage', label: 'Current Usage' },
    { key: 'skillDepth', label: 'Skill Depth' },
    { key: 'strategicThinking', label: 'Strategic Thinking' },
    { key: 'futureReadiness', label: 'Future Readiness' },
    { key: 'technicalFluency', label: 'Technical Fluency' },
  ];

  dimensions.forEach(dim => {
    const score = assessment.scores?.[dim.key] || 0;
    y = checkPageBreak(doc, y, 12);
    doc.setFontSize(10);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(60, 60, 60);
    doc.text(dim.label, 14, y);
    doc.text(`${score}%`, 100, y, { align: 'right' });

    // Score bar
    doc.setFillColor(230, 230, 235);
    doc.roundedRect(105, y - 4, 80, 5, 2, 2, 'F');
    const barColor = score >= 70 ? COLORS.green : score >= 40 ? COLORS.amber : COLORS.red;
    doc.setFillColor(...barColor);
    doc.roundedRect(105, y - 4, Math.max(1, (score / 100) * 80), 5, 2, 2, 'F');
    y += 10;
  });

  // Gap Analysis
  if (gapAnalysis) {
    y += 4;
    y = checkPageBreak(doc, y, 40);
    y = addSectionTitle(doc, y, 'North Star Gap Analysis');
    y = addText(doc, y, `Current Maturity: ${gapAnalysis.currentMaturity} / 5.0`, { bold: true, color: [...COLORS.primary] });
    y = addText(doc, y, `Target Maturity: ${gapAnalysis.targetMaturity} / 5.0`, { bold: true, color: [...COLORS.green] });
    const gapColor = gapAnalysis.gapSeverity === 'critical' ? COLORS.red : gapAnalysis.gapSeverity === 'moderate' ? COLORS.amber : COLORS.green;
    y = addText(doc, y, `Gap: ${gapAnalysis.gap} (${gapAnalysis.gapSeverity})`, { bold: true, color: gapColor });
    y += 2;
    y = addText(doc, y, gapAnalysis.transformation, { size: 9, color: [80, 80, 80] });
  }

  // Roadmap
  if (assessment.roadmap) {
    y += 4;
    y = checkPageBreak(doc, y, 30);
    y = addSectionTitle(doc, y, 'Personalized Roadmap');

    const phases = [
      { key: 'thirtyDays', label: '30 Days', color: COLORS.green },
      { key: 'sixtyDays', label: '60-90 Days', color: COLORS.amber },
      { key: 'sixMonths', label: '6 Months', color: COLORS.purple },
    ];

    phases.forEach(phase => {
      const items = assessment.roadmap[phase.key] || [];
      if (items.length === 0) return;
      y = checkPageBreak(doc, y, 20);
      doc.setFontSize(11);
      doc.setFont('helvetica', 'bold');
      doc.setTextColor(...phase.color);
      doc.text(phase.label, 14, y);
      y += 6;
      items.forEach(item => {
        y = checkPageBreak(doc, y, 8);
        y = addText(doc, y, `• ${item}`, { size: 9, color: [80, 80, 80], indent: 18, maxWidth: 165 });
      });
      y += 3;
    });
  }

  addFooter(doc);
  doc.save(`ai-readiness-results-${assessment.department}-${new Date().toISOString().slice(0, 10)}.pdf`);
}

