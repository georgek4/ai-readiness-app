// Export: Org Dashboard
// ============================================================
export function exportDashboardPDF(assessments, departments, northStarTargets, tierDefinitions) {
  const doc = createPDF('Organization Dashboard', 'landscape');
  const pageWidth = doc.internal.pageSize.getWidth();
  let y = addHeader(doc, 'Organization AI Readiness Dashboard', `${assessments.length} assessments across ${new Set(assessments.map(a => a.department)).size} departments`);

  // Summary stats
  const avgScore = Math.round(assessments.reduce((s, a) => s + a.overallScore, 0) / assessments.length * 10) / 10;
  y = addSectionTitle(doc, y, 'Executive Summary');
  const stats = [
    `Total Assessments: ${assessments.length}`,
    `Average Score: ${avgScore} / 100`,
    `Departments Assessed: ${new Set(assessments.map(a => a.department)).size}`,
    `Date Range: ${new Date(Math.min(...assessments.map(a => new Date(a.timestamp)))).toLocaleDateString()} — ${new Date(Math.max(...assessments.map(a => new Date(a.timestamp)))).toLocaleDateString()}`,
  ];
  stats.forEach(stat => {
    y = addText(doc, y, stat, { size: 10, color: [60, 60, 60] });
  });
  y += 4;

  // Tier Distribution
  y = checkPageBreak(doc, y, 40);
  y = addSectionTitle(doc, y, 'Tier Distribution');
  tierDefinitions.forEach(tier => {
    const count = assessments.filter(a => a.overallScore >= tier.min && a.overallScore <= tier.max).length;
    const rgb = hexToRgb(tier.color);
    doc.setFontSize(10);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(...rgb);
    doc.text(`${tier.name} (${tier.min}-${tier.max}): ${count} people`, 14, y);
    // Bar
    doc.setFillColor(230, 230, 235);
    doc.roundedRect(120, y - 4, 100, 5, 2, 2, 'F');
    const barWidth = assessments.length > 0 ? (count / assessments.length) * 100 : 0;
    doc.setFillColor(...rgb);
    doc.roundedRect(120, y - 4, Math.max(1, barWidth), 5, 2, 2, 'F');
    y += 8;
  });
  y += 4;

  // Department Scores
  y = checkPageBreak(doc, y, 30);
  y = addSectionTitle(doc, y, 'Department Scores vs. North Star Targets');

  // Table header
  doc.setFontSize(9);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(60, 60, 60);
  const cols = [14, 55, 78, 100, 120, 142, 164, 186, 210];
  ['Department', 'Avg', 'Aware', 'Usage', 'Depth', 'Strategy', 'Ready', 'Tech', 'Target'].forEach((h, i) => {
    doc.text(h, cols[i], y);
  });
  y += 2;
  doc.setDrawColor(200, 200, 210);
  doc.line(14, y, pageWidth - 14, y);
  y += 5;

  departments.forEach(dept => {
    const deptAssessments = assessments.filter(a => a.department === dept.id);
    if (deptAssessments.length === 0) return;
    y = checkPageBreak(doc, y, 8);

    const avg = Math.round(deptAssessments.reduce((s, a) => s + a.overallScore, 0) / deptAssessments.length);
    const dimAvg = (key) => Math.round(deptAssessments.reduce((s, a) => s + (a.scores?.[key] || 0), 0) / deptAssessments.length);
    const target = northStarTargets[dept.id]?.level || '-';

    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(60, 60, 60);
    doc.text(dept.name.slice(0, 18), cols[0], y);
    doc.text(`${avg}`, cols[1], y);
    doc.text(`${dimAvg('awareness')}`, cols[2], y);
    doc.text(`${dimAvg('currentUsage')}`, cols[3], y);
    doc.text(`${dimAvg('skillDepth')}`, cols[4], y);
    doc.text(`${dimAvg('strategicThinking')}`, cols[5], y);
    doc.text(`${dimAvg('futureReadiness')}`, cols[6], y);
    doc.text(`${dimAvg('technicalFluency')}`, cols[7], y);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(...COLORS.green);
    doc.text(`${target}`, cols[8], y);
    y += 7;
  });

  // Individual Assessments
  y += 6;
  y = checkPageBreak(doc, y, 30);
  y = addSectionTitle(doc, y, 'All Individual Assessments');

  doc.setFontSize(8);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(60, 60, 60);
  const aCols = [14, 50, 95, 135, 165, 200, 240];
  ['ID', 'Department', 'Role', 'Score', 'Tier', 'XP', 'Date'].forEach((h, i) => {
    doc.text(h, aCols[i], y);
  });
  y += 2;
  doc.line(14, y, pageWidth - 14, y);
  y += 5;

  assessments.sort((a, b) => new Date(b.timestamp) - new Date(a.timestamp)).forEach(a => {
    y = checkPageBreak(doc, y, 7);
    const dept = departments.find(d => d.id === a.department);
    doc.setFontSize(8);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(80, 80, 80);
    doc.text(a.anonymousId || `#${a.id}`, aCols[0], y);
    doc.text((dept?.name || a.department).slice(0, 20), aCols[1], y);
    doc.text((a.role || '').slice(0, 18), aCols[2], y);
    doc.text(`${a.overallScore}`, aCols[3], y);
    doc.text(a.tier || '', aCols[4], y);
    doc.text(`${a.xpTotal || 0}`, aCols[5], y);
    doc.text(new Date(a.timestamp).toLocaleDateString(), aCols[6], y);
    y += 6;
  });

  addFooter(doc);
  doc.save(`org-ai-readiness-dashboard-${new Date().toISOString().slice(0, 10)}.pdf`);
}

function hexToRgb(hex) {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  return result ? [parseInt(result[1], 16), parseInt(result[2], 16), parseInt(result[3], 16)] : [100, 100, 100];
}
