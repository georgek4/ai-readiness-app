import jsPDF from 'jspdf';

const COLORS = {
  primary: [59, 130, 246],
  dark: [15, 23, 42],
  card: [30, 41, 59],
  text: [241, 245, 249],
  textSecondary: [148, 163, 184],
  textMuted: [100, 116, 139],
  green: [16, 185, 129],
  amber: [245, 158, 11],
  red: [239, 68, 68],
  purple: [139, 92, 246],
  blue: [6, 182, 212],
};

function createPDF(title, orientation = 'portrait') {
  const doc = new jsPDF({ orientation, unit: 'mm', format: 'a4' });
  doc.setFont('helvetica');
  return doc;
}

function addHeader(doc, title, subtitle) {
  const pageWidth = doc.internal.pageSize.getWidth();
  // Header bar
  doc.setFillColor(...COLORS.primary);
  doc.rect(0, 0, pageWidth, 28, 'F');
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(18);
  doc.setFont('helvetica', 'bold');
  doc.text(title, 14, 14);
  if (subtitle) {
    doc.setFontSize(10);
    doc.setFont('helvetica', 'normal');
    doc.text(subtitle, 14, 22);
  }
  // Branding
  doc.setFontSize(8);
  doc.text('AI Readiness Assessment Platform', pageWidth - 14, 14, { align: 'right' });
  doc.text(new Date().toLocaleDateString(), pageWidth - 14, 22, { align: 'right' });
  return 36;
}

function addSectionTitle(doc, y, text, color = COLORS.primary) {
  const pageWidth = doc.internal.pageSize.getWidth();
  doc.setFillColor(...color);
  doc.rect(14, y - 1, 3, 8, 'F');
  doc.setTextColor(...color);
  doc.setFontSize(13);
  doc.setFont('helvetica', 'bold');
  doc.text(text, 20, y + 5);
  doc.setDrawColor(200, 200, 210);
  doc.line(14, y + 9, pageWidth - 14, y + 9);
  return y + 14;
}

function addText(doc, y, text, { size = 10, color = [60, 60, 60], bold = false, maxWidth = 170, indent = 14 } = {}) {
  doc.setFontSize(size);
  doc.setFont('helvetica', bold ? 'bold' : 'normal');
  doc.setTextColor(...color);
  const lines = doc.splitTextToSize(text, maxWidth);
  doc.text(lines, indent, y);
  return y + lines.length * (size * 0.45) + 2;
}

function checkPageBreak(doc, y, needed = 30) {
  const pageHeight = doc.internal.pageSize.getHeight();
  if (y + needed > pageHeight - 20) {
    doc.addPage();
    return 20;
  }
  return y;
}

function addFooter(doc) {
  const pageCount = doc.internal.getNumberOfPages();
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    const pageWidth = doc.internal.pageSize.getWidth();
    const pageHeight = doc.internal.pageSize.getHeight();
    doc.setFontSize(8);
    doc.setTextColor(150, 150, 150);
    doc.text(`Page ${i} of ${pageCount}`, pageWidth / 2, pageHeight - 8, { align: 'center' });
    doc.text('Generated by AI Readiness Assessment Platform', 14, pageHeight - 8);
  }
}

