// Export: Department Explorer
// ============================================================
export function exportDepartmentPDF(dept, classifications, northStarTarget) {
  const doc = createPDF('Department Report');
  let y = addHeader(doc, `${dept.icon} ${dept.name}`, dept.mission);

  // North Star
  if (northStarTarget) {
    y = addSectionTitle(doc, y, `North Star Target: Level ${northStarTarget.level}`);
    y = addText(doc, y, northStarTarget.transformation, { size: 9, color: [80, 80, 80] });
    y += 4;
  }

  // Roles
  y = checkPageBreak(doc, y, 20);
  y = addSectionTitle(doc, y, 'Typical Roles');
  y = addText(doc, y, dept.roles.join(', '), { size: 9, color: [80, 80, 80] });
  y += 4;

  // Metrics
  y = checkPageBreak(doc, y, 20);
  y = addSectionTitle(doc, y, 'Core Metrics');
  y = addText(doc, y, dept.metrics.join(', '), { size: 9, color: [80, 80, 80] });
  y += 4;

  // Activities by classification
  const classOrder = ['humanize', 'agentify', 'automate'];
  const classColors = { humanize: COLORS.green, agentify: COLORS.amber, automate: COLORS.red };
  const classNames = { humanize: 'Humanize', agentify: 'Agentify', automate: 'Automate' };

  classOrder.forEach(cls => {
    const items = dept.activities.filter(a => a.classification === cls);
    if (items.length === 0) return;

    y = checkPageBreak(doc, y, 30);
    y = addSectionTitle(doc, y, `${classNames[cls]} Activities (${items.length})`, classColors[cls]);

    items.forEach(activity => {
      y = checkPageBreak(doc, y, 45);

      // Activity name
      doc.setFontSize(11);
      doc.setFont('helvetica', 'bold');
      doc.setTextColor(40, 40, 40);
      doc.text(activity.name, 14, y);
      y += 5;

      // Short description
      y = addText(doc, y, activity.description, { size: 9, color: [80, 80, 80], bold: true });

      // Detailed description
      if (activity.detailedDescription) {
        y = addText(doc, y, activity.detailedDescription, { size: 9, color: [90, 90, 90] });
      }

      // AI Opportunity
      if (activity.aiOpportunity) {
        y = checkPageBreak(doc, y, 15);
        doc.setFontSize(9);
        doc.setFont('helvetica', 'bold');
        doc.setTextColor(...classColors[cls]);
        doc.text('AI Opportunity:', 18, y);
        y += 4;
        y = addText(doc, y, activity.aiOpportunity, { size: 8, color: [100, 100, 100], indent: 18, maxWidth: 162 });
      }

      // Tools
      if (activity.exampleTools?.length) {
        y = checkPageBreak(doc, y, 10);
        doc.setFontSize(9);
        doc.setFont('helvetica', 'bold');
        doc.setTextColor(60, 60, 60);
        doc.text('Recommended Tools:', 18, y);
        y += 4;
        activity.exampleTools.forEach(tool => {
          y = addText(doc, y, `• ${tool.name} — ${tool.url}`, { size: 8, color: [80, 80, 150], indent: 22, maxWidth: 158 });
        });
      }

      // Implementation Tips
      if (activity.implementationTips?.length) {
        y = checkPageBreak(doc, y, 15);
        doc.setFontSize(9);
        doc.setFont('helvetica', 'bold');
        doc.setTextColor(60, 60, 60);
        doc.text('Implementation Tips:', 18, y);
        y += 4;
        activity.implementationTips.forEach(tip => {
          y = checkPageBreak(doc, y, 8);
          y = addText(doc, y, `• ${tip}`, { size: 8, color: [100, 100, 100], indent: 22, maxWidth: 158 });
        });
      }

      // Maturity Indicators
      if (activity.maturityIndicators) {
        y = checkPageBreak(doc, y, 20);
        doc.setFontSize(9);
        doc.setFont('helvetica', 'bold');
        doc.setTextColor(60, 60, 60);
        doc.text('Maturity Levels:', 18, y);
        y += 4;
        const mi = activity.maturityIndicators;
        y = addText(doc, y, `Beginner: ${mi.beginner}`, { size: 8, color: [160, 80, 80], indent: 22, maxWidth: 158 });
        y = addText(doc, y, `Intermediate: ${mi.intermediate}`, { size: 8, color: [160, 130, 40], indent: 22, maxWidth: 158 });
        y = addText(doc, y, `Advanced: ${mi.advanced}`, { size: 8, color: [40, 140, 100], indent: 22, maxWidth: 158 });
      }

      // Rationale
      doc.setFontSize(8);
      doc.setFont('helvetica', 'italic');
      doc.setTextColor(140, 140, 140);
      y = checkPageBreak(doc, y, 8);
      const rationaleLines = doc.splitTextToSize(`Classification rationale: ${activity.rationale}`, 162);
      doc.text(rationaleLines, 18, y);
      y += rationaleLines.length * 3.5 + 6;
    });
  });

  addFooter(doc);
  doc.save(`department-${dept.id}-ai-classification-report.pdf`);
}

